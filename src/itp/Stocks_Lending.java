         double ssQQ3 = ss3*QQ3;
         double ssQQ4 = ss4*QQ4;
         double ssQQ5 = ss5*QQ5;
         
         double pri = ssQQ1 + ssQQ2 + ssQQ3 + ssQQ4 + ssQQ5;
         
         String sQ1 = String.valueOf(ssQQ1);
         String sQ2 = String.valueOf(ssQQ2);
         String sQ3 = String.valueOf(ssQQ3);
         String sQ4 = String.valueOf(ssQQ4);
         String sQ5 = String.valueOf(ssQQ5);
         
         String tot1 = String.valueOf(tot);
         String pri1 = String.valueOf(pri);
         
         lblTP1.setText(sQ1);
         lblTP2.setText(sQ2);
         lblTP3.setText(sQ3);
         lblTP4.setText(sQ4);
         lblTP5.setText(sQ5);
         
         lblNOIQ.setText(tot1);
         lblNOIQP.setText(pri1);
