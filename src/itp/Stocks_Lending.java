                v.add(rs.getString("qty1"));
                v.add(rs.getString("item_no2"));
                v.add(rs.getString("qty2"));
                v.add(rs.getString("item_no3"));
                v.add(rs.getString("qty3"));
                v.add(rs.getString("item_no4"));
                v.add(rs.getString("qty4"));
                v.add(rs.getString("item_no5"));
